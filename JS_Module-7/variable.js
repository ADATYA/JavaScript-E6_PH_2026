// Interview Tip ⭐

// Question: let এবং const-এর মধ্যে পার্থক্য কী?

// Answer:

// let দিয়ে তৈরি Variable-এর Value পরে পরিবর্তন করা যায়।
// const দিয়ে তৈরি Variable-এর Value পরে পরিবর্তন করা যায় না।
// সাধারণভাবে, যেসব Value পরিবর্তন হওয়ার কথা নয় সেগুলোর জন্য const ব্যবহার করা ভালো।